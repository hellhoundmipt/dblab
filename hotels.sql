CREATE TABLE HOTELS (
	hotel_id integer NOT NULL PRIMARY KEY,
	hotel_name varchar(100) NOT NULL,
	stars integer NOT NULL,
	resept_start_time time NOT NULL,
	resept_end_time time NOT NULL,
	email varchar(320),
	phone_number varchar(20),
	web_site varchar(320));


CREATE TABLE ADDRESS (
	addr_id integer NOT NULL PRIMARY KEY,
	locality_id integer NOT NULL,
	street varchar(50) NOT NULL,
	building integer NOT NULL,
	zip integer NOT NULL);


CREATE TABLE SUITE (
	suite_id integer NOT NULL PRIMARY KEY,
	suite_number integer NOT NULL,
	hotel_id integer NOT NULL,
	max_capacity integer NOT NULL,
	status varchar(10) NOT NULL);



CREATE TABLE FACILITIES (
	facility_id integer NOT NULL PRIMARY KEY,
	hotel_id integer NOT NULL,
	name varchar(30) NOT NULL,
	price integer NOT NULL);



CREATE TABLE PRICE (
	price_id integer NOT NULL PRIMARY KEY,
	suite_id integer,
	price integer NOT NULL,
	discount integer,
	start_date date NOT NULL,
	end_date datetime);


CREATE TABLE REVIEW (
	review_id integer NOT NULL PRIMARY KEY,
	traveler_id integer NOT NULL,
	hotel_id integer,
	rating integer NOT NULL,
	review varchar(300) NOT NULL,
	review_date datetime NOT NULL);


CREATE TABLE LOCALITY (
	locality_id integer NOT NULL PRIMARY KEY,
	name varchar(30) NOT NULL,
	type integer NOT NULL,
	country_id integer NOT NULL);


CREATE TABLE COUNTRY (
	country_id integer NOT NULL PRIMARY KEY,
	name varchar(30) NOT NULL);


CREATE TABLE LOCALITY_TYPE (
	type_id integer NOT NULL PRIMARY KEY,
	name varchar(30) NOT NULL);


CREATE TABLE TRAVELER (
	traveler_id integer NOT NULL PRIMARY KEY,
	nickname varchar(30) NOT NULL UNIQUE,
	first_name varchar(30),
	second_name varchar(30),
	age date,
	email varchar(320) NOT NULL,
	sign_in_date date NOT NULL);


CREATE TABLE SUBSCRIBTION_POOL (
	subscribtion_id integer NOT NULL PRIMARY KEY,
	traveler_id integer NOT NULL,
	subscr_id integer NOT NULL);


CREATE TABLE COMMENTS (
	comment_id integer NOT NULL PRIMARY KEY,
	traveler_id integer NOT NULL,
	comment varchar(300) NOT NULL,
	comment_date date NOT NULL,
	review_id integer NOT NULL);


